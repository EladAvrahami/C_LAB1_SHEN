#include <stdio.h>

// https://www.geeksforgeeks.org/printf-in-c/
int main()
{
    float num;

    // קבלת קלט מהמשתמש
    printf("Enter a real number: ");
    scanf("%f", &num);

    // הדפסת המספר עם 4 ספרות לפני הנקודה ו-3 ספרות אחרי הנקודה
    printf("%08.3f\n", num);

    return 0;
}
/*
הסבר:
scanf("%f", &num); – קבלת מספר ממשי (float) מהמשתמש.
printf("%07.3f", num); – הדפסת המספר לפי פורמט המוגדר:
% – מציין שזהו פורמט הדפסה.
0 – מציין שאם יש חוסר ספרות לפני הנקודה, יש להשלים את המקום עם אפסים.
7 – מציין את מספר התווים הכללי (כולל ספרות שלמות ואחוזת נקודה עשרונית) שיש להדפיס.
.3 – מציין שיש להדפיס 3 ספרות אחרי הנקודה.
f – מציין שמדובר במספר ממשי (float).
*/