#include <stdio.h>

// כמה פעולות  יש בתוכנית ? 
//תשובה 1: יש 2 לולאות פור ובתוכן יש פעולה אחת קבועה ולכן סה"כ 2 פעולות

//מהי היעילות/סיבוכיות של הקוד ביחס ל n?
//תשובה 2: O(n^2) כי יש לולאה פנימית שרצה על כל המערך ובכל איטרציה יש לולאה נוספת שרצה על כל המערך

int main(){
    int i ,j,n;
    scanf("%d",&n);
    for(i=0;i<n;i++){
        printf("%d - ",i);
        for(j=0;j<i;j++){
            printf("%d - ",j);
        }
        printf("\n");
    }



    return 0;
}